import math

# Ceil of 2.3
ceil_result = math.ceil(2.3)
print(f"Ceil of 2.3: {ceil_result}")

# Floor of 2.3
floor_result = math.floor(2.3)
print(f"Floor of 2.3: {floor_result}")

# Factorial of 5
factorial_result = math.factorial(5)
print(f"Factorial of 5: {factorial_result}")

# 2^3
power_result = math.pow(2, 3)
print(f"2^3: {power_result}")

# Square root of 16
sqrt_result = math.sqrt(16)
print(f"Square root of 16: {sqrt_result}")